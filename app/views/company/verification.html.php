<h3>Verification Page</h3>
<p>Though IBWT is not regulated by HMRC, FCA and KYC/AML/MSB standards, we still require a certain level of verification and identity checks. This helps us prevent fraud, keep customers accounts secure and ensure best business practices.</p>
<p>Our aim is to inspire trust with customers and establishments to build long lasting relationships.</p>
<p>To become IBWT customer<sup>1</sup> and use our platform and services, you must submit the following information:
<ul>
<li>Full name</li>
<li>Government issued photo identification</li>
<li>Proof of address (utility bill, credit statement, or official recognised* letter, NOT mobile phone bill)</li>
<li>Bank details for linked bank account, must be in customers own name (account number, sort code, account name).</li>
<li>Contact telephone number.</li>
<li>Contact email.</li>
</ul>
</p>
<p>IBWT reserve the right to seek more information to verify a customer's identity, and/or to refuse an account if the customer's identity is not verified to our legal requirement.</p>
<p>Your information is subject to our <a href="/company/privacy">Privacy Policy</a>.</p>
