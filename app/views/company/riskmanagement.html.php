<h3><?=$t("RISK MANAGEMENT")?></h3>

<p>We aim to be a leading example of best practices and provide a secure and safe environment for you to transact with digital currency. As such our criteria may seem above and beyond the legal requirements, but they are here to protect you and the sanctity of digital currency.</p>
<p>We have three tiers of customers, and customers can process bank wires to OKPAY to fund their IBWT account.</p>
<blockquote>
<ul class="unstyled">
<li><strong>Registered</strong> - can deposit/withdraw bitcoins or accepted alt-coins and use the trading platform.</li>
 
<li><strong>(For Royal Mail deposits and withdrawals.)</strong>
<ul>
<li><strong>Registered</strong> - You can deposit funds up to &pound;2,500 per day via Royal Mail Special Delivery Service.</li>
<li><strong>Verified</strong> (with proof of address) - You can deposit up to &pound;5,000 a day.</li>
<li><strong>Fully Verified</strong> (with government photo ID) - You can deposit up to &pound;20,000 a day.</li>
</ul>
</li>
</ul>
</blockquote>
<p>Please see our Method of Deposit page for more details.</p>
 
<p><strong>(For OKPAY deposits and withdrawals.)</strong></p>
<blockquote>
 <ul class="unstyled">
<li>You can conduct bank wire transfers and credit your IBWT account via OKPAY, please see our OKPAY information page for more details.</li>
<li>OKPAY operate their own verification criteria which can be seen here in more details. You will need to provide the basic proof of identity documents for this method of deposit and withdrawal.</li>
<li>OKPAY has various methods of funding all of which are at your disposal for funding your IBWT account.</li>
 </ul>
 </blockquote>
<p>All deposits and withdrawals need to be verified and confirmed.</p>
<p>If transactions are unusual and/or suspicious we reserve the right to investigate and freeze related accounts.</p>
<p>Suspicious activity on accounts will be queried and investigated; monies may be returned (or frozen). Any such cases may result in further investigation and a report filed to the relevant authority.</p>
<p>Trying to bypass account limits or conduct international money transfers may result in the related accounts banned (email, username, mobile, ip address tracked and blocked) and all funds refunded if possible.</p>
<p>Random security checks are done on all accounts, you may receive an email in relation to this.</p>
<p>There are no account limits.</p>
<p>Fiat deposited is stored securely via safety deposit boxes.</p>
<p>There is no deposit or withdrawal limit in Bitcoin or Litecoin.</p>
<p>Any concerns can be directed FAO to the Risk Management Officer via support@ibwt.co.uk.</p>
<p><sup>1</sup> Suspicious meaning but not limited to, activity out of the ordinary, large movements of value, suspected identity fraud, attempts to bypass account limits or conduct international money transfers.</p>
